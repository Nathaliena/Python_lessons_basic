# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

import random
import re

with open(r'C:\Users\Наташа\Desktop\re.txt', 'w') as my_file:
    for i in range(2500):
        my_file.write(str(random.randint(0, 9)))
with open(r'C:\Users\Наташа\Desktop\re.txt', 'r') as my_file:
    longest = list()
    huge = my_file.read()
    for number in range(0, 10):
        if str(number) in huge:
            lst = re.findall('[%s]+' % number, huge)
            longest.append(max(lst, key=len))
    result = max(longest, key=len)
    print(result)
